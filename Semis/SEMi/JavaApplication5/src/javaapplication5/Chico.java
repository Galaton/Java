import java.util.Scanner;

public class Chico{
    public static void main(String[] args){
        int chico = 150;
        int ze = 110;
        int ano = 0;
        while(chico>ze){
            
            chico=chico+2;
            ze=ze+3;
            ano++;
            
        }
        System.out.println(ano);
    }
    
}